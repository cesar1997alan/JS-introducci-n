// Números en js no importa que tipo de valor tenga, negativo, decimal, etc. 
const numero1 = 100;
const numero2 = 200;
const numero3 = 30;
const numero4 = 15;
const numero5 = 270;



console.log(numero1 + numero2) // suma
console.log(numero2 - numero3) // resta
console.log(numero3 * numero4) // multiplicación
console.log(numero4 / numero5) // división 
console.log(numero5 % numero1) // modulo (nos arroja el residuo como si fuera una división)